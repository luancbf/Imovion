'use client';

import { useState, useCallback } from 'react';
import { SliderBanner } from '@/types/cadastrar-patrocinador';

// ‚úÖ Configura√ß√£o das imagens dispon√≠veis
const availableSliderImages = [
  { name: 'banner1', title: 'Banner Principal', description: 'Primeira posi√ß√£o no slider' },
  { name: 'banner2', title: 'Banner Secund√°rio', description: 'Segunda posi√ß√£o no slider' },
  { name: 'banner3', title: 'Banner Terci√°rio', description: 'Terceira posi√ß√£o no slider' }
];

export const useSliderConfig = () => {
  const [sliderBanners, setSliderBanners] = useState<SliderBanner[]>([]);
  const [loading, setLoading] = useState(false);
  const [uploadingImages, setUploadingImages] = useState<Record<string, boolean>>({});

  // ‚úÖ INFORMA√á√ïES DA IMAGEM
  const getSliderImageInfo = useCallback((imageName: string) => {
    return availableSliderImages.find(img => img.name === imageName) || {
      name: imageName,
      title: imageName,
      description: 'Banner do slider'
    };
  }, []);

  // ‚úÖ CARREGAR BANNERS (corrigido para usar os campos corretos)
  const loadSliderBanners = useCallback(async () => {
    console.log('üì• [SLIDER] Iniciando carregamento dos banners...');
    setLoading(true);
    
    try {
      const { createBrowserClient } = await import("@supabase/ssr");
      const supabase = createBrowserClient(
        process.env.NEXT_PUBLIC_SUPABASE_URL!,
        process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!
      );

      const { data, error } = await supabase
        .from('slider_banners')
        .select(`
          *,
          patrocinadores (
            id,
            nome,
            slug
          )
        `)
        .order('order_index');
      
      if (error) {
        console.error('‚ùå [SLIDER] Erro ao carregar do banco:', error);
        console.log('üîÑ [SLIDER] Criando banners mock...');
        
        // Criar banners mock
        const mockBanners: SliderBanner[] = availableSliderImages.map((imageConfig, index) => ({
          id: `mock-${index}`,
          image_name: imageConfig.name,
          image_url: null,
          image_alt: null,
          patrocinador_id: null,
          is_active: false,
          is_clickable: false,
          order_index: index,
          created_at: new Date().toISOString(),
          updated_at: new Date().toISOString(),
          patrocinadores: null
        }));
        
        setSliderBanners(mockBanners);
        return;
      }
      
      console.log('üìä [SLIDER] Dados do banco:', data);
      
      // Mapear dados existentes
      const bannersMap = new Map(data?.map(b => [b.image_name, b]) || []);
      const allBanners: SliderBanner[] = [];
      
      // Garantir que todos os banners existam
      availableSliderImages.forEach((imageConfig, index) => {
        const existingBanner = bannersMap.get(imageConfig.name);
        if (existingBanner) {
          console.log(`‚úÖ [SLIDER] Banner existente: ${imageConfig.name} ID: ${existingBanner.id}`);
          allBanners.push(existingBanner);
        } else {
          console.log(`‚ûï [SLIDER] Criando banner vazio: ${imageConfig.name}`);
          allBanners.push({
            image_name: imageConfig.name,
            image_url: null,
            image_alt: null,
            patrocinador_id: null,
            is_active: false,
            is_clickable: false,
            order_index: index,
            created_at: new Date().toISOString(),
            updated_at: new Date().toISOString(),
            patrocinadores: null
          });
        }
      });
      
      console.log('üìã [SLIDER] Total de banners:', allBanners.length);
      setSliderBanners(allBanners);
      
    } catch (error) {
      console.error('‚ùå [SLIDER] Erro no carregamento:', error);
      
      // Fallback para mock
      const mockBanners: SliderBanner[] = availableSliderImages.map((imageConfig, index) => ({
        id: `mock-${index}`,
        image_name: imageConfig.name,
        image_url: null,
        image_alt: null,
        patrocinador_id: null,
        is_active: false,
        is_clickable: false,
        order_index: index,
        created_at: new Date().toISOString(),
        updated_at: new Date().toISOString(),
        patrocinadores: null
      }));
      
      console.log('üîÑ [SLIDER MOCK] Usando dados mock');
      setSliderBanners(mockBanners);
      
    } finally {
      setLoading(false);
    }
  }, []);

  // ‚úÖ ATUALIZAR BANNER NO ESTADO
  const updateSliderBanner = useCallback((imageName: string, field: keyof SliderBanner, value: string | boolean | number | null) => {
    console.log(`üîÑ [SLIDER UPDATE] ${imageName}.${field}:`, value);
    setSliderBanners(prev => 
      prev.map(banner => 
        banner.image_name === imageName 
          ? { ...banner, [field]: value }
          : banner
      )
    );
  }, []);

  // ‚úÖ CONTROLE DE UPLOAD
  const setImageUploading = useCallback((imageName: string, uploading: boolean) => {
    setUploadingImages(prev => ({ ...prev, [imageName]: uploading }));
  }, []);

  // ‚úÖ SALVAR BANNER (corrigido para usar order_index)
  const saveSliderBanner = useCallback(async (imageName: string): Promise<void> => {
    const banner = sliderBanners.find(b => b.image_name === imageName);
    if (!banner) {
      throw new Error('Banner n√£o encontrado');
    }

    console.log(`üíæ [SLIDER SAVE] Salvando banner: ${imageName}`, banner);

    // ‚úÖ VALIDA√á√ÉO: Banner pode ser ativo apenas com imagem
    if (banner.is_active && (!banner.image_url || banner.image_url.trim() === '')) {
      throw new Error('√â necess√°rio enviar uma imagem para ativar o banner');
    }

    // ‚úÖ VALIDA√á√ÉO: Clic√°vel requer patrocinador
    if (banner.is_active && banner.is_clickable && (!banner.patrocinador_id || banner.patrocinador_id.trim() === '')) {
      throw new Error('Para modo clic√°vel, √© necess√°rio selecionar um patrocinador');
    }

    try {
      const { createBrowserClient } = await import("@supabase/ssr");
      const supabase = createBrowserClient(
        process.env.NEXT_PUBLIC_SUPABASE_URL!,
        process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!
      );

      // ‚úÖ CORRIGIDO: Dados para salvar com campos corretos
      const saveData = {
        image_name: banner.image_name,
        image_url: banner.image_url,
        image_alt: banner.image_alt,
        patrocinador_id: (banner.patrocinador_id && banner.patrocinador_id !== '') ? banner.patrocinador_id : null,
        is_active: banner.is_active,
        is_clickable: banner.is_clickable,
        order_index: banner.order_index, // ‚úÖ CORRIGIDO: usar order_index ao inv√©s de display_order
        updated_at: new Date().toISOString()
      };

      if (banner.id && !banner.id.startsWith('mock-')) {
        // ‚úÖ ATUALIZAR registro existente
        console.log(`üîÑ [SLIDER UPDATE] Atualizando ID: ${banner.id}`);
        
        const { data, error } = await supabase
          .from('slider_banners')
          .update(saveData)
          .eq('id', banner.id)
          .select(`
            *,
            patrocinadores (
              id,
              nome,
              slug
            )
          `);

        if (error) {
          console.error('‚ùå [SLIDER UPDATE ERROR]:', error);
          throw new Error(`Erro ao atualizar: ${error.message}`);
        }
        
        console.log('‚úÖ [SLIDER UPDATE SUCCESS]:', data);
      } else {
        // ‚úÖ INSERIR novo registro
        console.log(`‚ûï [SLIDER INSERT] Criando novo banner: ${imageName}`);
        
        const { data, error } = await supabase
          .from('slider_banners')
          .upsert(saveData, {
            onConflict: 'image_name'
          })
          .select(`
            *,
            patrocinadores (
              id,
              nome,
              slug
            )
          `);

        if (error) {
          console.error('‚ùå [SLIDER INSERT ERROR]:', error);
          throw new Error(`Erro ao salvar: ${error.message}`);
        }
        
        console.log('‚úÖ [SLIDER INSERT SUCCESS]:', data);
      }

      // Recarregar banners
      await loadSliderBanners();
      
    } catch (error) {
      console.error('‚ùå [SLIDER SAVE ERROR]:', error);
      
      // Se der erro de tabela n√£o existir, simular salvamento
      if (error instanceof Error && error.message.includes('relation') && error.message.includes('does not exist')) {
        console.log('üîÑ [SLIDER MOCK SAVE] Simulando salvamento...');
        setSliderBanners(prev => 
          prev.map(b => 
            b.image_name === imageName 
              ? { ...b, updated_at: new Date().toISOString() }
              : b
          )
        );
        return;
      }
      
      throw error;
    }
  }, [sliderBanners, loadSliderBanners]);

  // ‚úÖ DELETAR BANNER
  const deleteSliderBanner = useCallback(async (imageName: string): Promise<void> => {
    const banner = sliderBanners.find(b => b.image_name === imageName);
    if (!banner?.id || banner.id.startsWith('mock-')) {
      throw new Error('Banner n√£o encontrado ou n√£o salvo');
    }

    console.log(`üóëÔ∏è [SLIDER DELETE] Deletando banner: ${imageName} ID: ${banner.id}`);

    try {
      const { createBrowserClient } = await import("@supabase/ssr");
      const supabase = createBrowserClient(
        process.env.NEXT_PUBLIC_SUPABASE_URL!,
        process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!
      );

      const { error } = await supabase
        .from('slider_banners')
        .delete()
        .eq('id', banner.id);

      if (error) {
        console.error('‚ùå [SLIDER DELETE ERROR]:', error);
        throw new Error(`Erro ao excluir: ${error.message}`);
      }
      
      console.log('‚úÖ [SLIDER DELETE SUCCESS]');
      
      // Recarregar banners
      await loadSliderBanners();
    } catch (error) {
      console.error('‚ùå [SLIDER DELETE CATCH]:', error);
      throw error;
    }
  }, [sliderBanners, loadSliderBanners]);

  // ‚úÖ RESETAR BANNER
  const resetSliderBanner = useCallback((imageName: string) => {
    console.log(`üîÑ [SLIDER RESET] Resetando banner: ${imageName}`);
    
    setSliderBanners(prev => 
      prev.map(banner => 
        banner.image_name === imageName 
          ? {
              ...banner,
              image_url: null,
              image_alt: null,
              patrocinador_id: null,
              is_active: false,
              is_clickable: false
            }
          : banner
      )
    );
  }, []);

  // ‚úÖ VALIDA√á√ÉO
  const validateSliderBanner = useCallback((imageName: string): { valid: boolean; error?: string } => {
    const banner = sliderBanners.find(b => b.image_name === imageName);
    if (!banner) {
      return { valid: false, error: 'Banner n√£o encontrado' };
    }

    if (banner.is_active) {
      if (!banner.image_url || banner.image_url.trim() === '') {
        return { valid: false, error: 'Envie uma imagem para ativar o banner' };
      }
      
      if (banner.is_clickable && (!banner.patrocinador_id || banner.patrocinador_id.trim() === '')) {
        return { valid: false, error: 'Selecione um patrocinador para modo clic√°vel' };
      }
    }

    return { valid: true };
  }, [sliderBanners]);

  // ‚úÖ UTILIT√ÅRIOS
  const getActiveBanners = useCallback(() => {
    return sliderBanners.filter(banner => 
      banner.is_active && 
      banner.image_url && 
      banner.image_url.trim() !== ''
    );
  }, [sliderBanners]);

  const getClickableBanners = useCallback(() => {
    return sliderBanners.filter(banner => 
      banner.is_active && 
      banner.is_clickable && 
      banner.patrocinador_id && 
      banner.patrocinador_id.trim() !== ''
    );
  }, [sliderBanners]);

  const getTotalActiveBanners = useCallback(() => {
    return getActiveBanners().length;
  }, [getActiveBanners]);

  const getTotalClickableBanners = useCallback(() => {
    return getClickableBanners().length;
  }, [getClickableBanners]);

  const getBannersWithImages = useCallback(() => {
    return sliderBanners.filter(banner => 
      banner.image_url && 
      banner.image_url.trim() !== ''
    );
  }, [sliderBanners]);

  return {
    sliderBanners,
    loading,
    uploadingImages,
    loadSliderBanners,
    updateSliderBanner,
    setImageUploading,
    saveSliderBanner,
    deleteSliderBanner,
    resetSliderBanner,
    getSliderImageInfo,
    validateSliderBanner,
    getActiveBanners,
    getClickableBanners,
    getTotalActiveBanners,
    getTotalClickableBanners,
    getBannersWithImages
  } as const;
};